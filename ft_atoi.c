int		ft_atoi(const char *str)
{
	int arr[2];

	arr[0] = 0;
	arr[1] = 0;
	while (*str == ' ' || (*str > 9 && *str < 14) || *str == '+')
		str++;
	if (*str++ == '-')
		arr[0] = 1;
	while (*str > 47 && *str < 58)
	{
		arr[1] = arr[1] * 10 + *str - 48;
		str++;
	}
	if (arr[0])
		return (-arr[1]);
	return (arr[1]);
}
