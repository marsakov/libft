#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	if ((unsigned char)c == '\0')
	{
		while (*(unsigned char *)s)
			s++;
		return ((void *)s);
	}
	while (n--)
	{
		if (*(unsigned char *)s == (unsigned char)c)
			return ((void *)s);
		s++;
	}
	return (NULL);
}
